package de.calculator;


public class Calculator {

    public double add(double i1, double i2) {
        return i1 + i2;
    }
    public double remove(double i1, double i2){
        return i1 - i2;
    }
    public double multiple(double i1, double i2){
        return i1 * i2;
    }
    public double divide(double i1, double i2){
        return i1 / i2;
    }

}
